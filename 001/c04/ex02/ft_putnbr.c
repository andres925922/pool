#include	<unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	m;

	m = 1000000000;
	if (nb < 0)
	{
		nb = nb *(-1);
		ft_putchar ('-');
	}
	while (m >= 1)
	{
		if (nb >= m)
			ft_putchar ((nb / m % 10) + '0');
		m = m / 10;
	}
	if (nb == -2147483648)
		write (1, "2147483648", 10);
	if (nb == 0)
		ft_putchar ('0');
}
